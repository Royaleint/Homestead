-- Direct lookup by NPC ID: O(1)
function VendorDatabase:GetVendor(npcID)
    local vendor = self.Vendors[npcID]
    if vendor then
        vendor.npcID = npcID
    end
    return vendor
end

-- Check if vendor exists
function VendorDatabase:HasVendor(npcID)
    return self.Vendors[npcID] ~= nil
end

-- Get all vendors (cached  result is rebuilt only when InvalidateVendorCache is called)
local cachedAllVendors = nil

function VendorDatabase:GetAllVendors()
    if cachedAllVendors then return cachedAllVendors end
    local all = {}
    for npcID, vendor in pairs(self.Vendors) do
        vendor.npcID = npcID
        all[#all + 1] = vendor
    end
    cachedAllVendors = all
    return all
end

function VendorDatabase:InvalidateVendorCache()
    cachedAllVendors = nil
end

-- Get vendors by map ID
function VendorDatabase:GetVendorsByMapID(mapID)
    local vendors = {}
    local npcIDs = self.ByMapID and self.ByMapID[mapID]
    if npcIDs then
        for _, npcID in ipairs(npcIDs) do
            local vendor = self.Vendors[npcID]
            if vendor then
                vendor.npcID = npcID
                table.insert(vendors, vendor)
            end
        end
    end
    return vendors
end

-- Get vendors by expansion
function VendorDatabase:GetVendorsByExpansion(expansion)
    local vendors = {}
    local npcIDs = self.ByExpansion and self.ByExpansion[expansion]
    if npcIDs then
        for _, npcID in ipairs(npcIDs) do
            local vendor = self.Vendors[npcID]
            if vendor then
                vendor.npcID = npcID
                table.insert(vendors, vendor)
            end
        end
    end
    return vendors
end

-- Find vendor by name (case-insensitive)
function VendorDatabase:FindVendorByName(name)
    if not name then return nil end
    local npcID = self.ByName and self.ByName[name:lower()]
    if npcID then
        local vendor = self.Vendors[npcID]
        if vendor then
            vendor.npcID = npcID
            return vendor
        end
    end
    return nil
end

-- Get total vendor count
function VendorDatabase:GetVendorCount()
    local count = 0
    for _ in pairs(self.Vendors) do
        count = count + 1
    end
    return count
end

function VendorDatabase:GetAliasCount()
    local staticCount = 0
    for _ in pairs(self.Aliases) do
        staticCount = staticCount + 1
    end
    local discoveredCount = 0
    if HomesteadDB and HomesteadDB.global and HomesteadDB.global.discoveredAliases then
        for _ in pairs(HomesteadDB.global.discoveredAliases) do
            discoveredCount = discoveredCount + 1
        end
    end
    return staticCount, discoveredCount
end

