-- Called once on ADDON_LOADED to build lookup indexes
function VendorDatabase:BuildIndexes()
    self.ByMapID = {}
    self.ByExpansion = {}
    self.ByItemID = {}  -- Reverse index: itemID -> { npcID1, npcID2, ... }

    local vendorCount = 0
    local itemCount = 0

    for npcID, vendor in pairs(self.Vendors) do
        vendorCount = vendorCount + 1
        vendor.npcID = npcID

        -- Index by mapID
        local mapID = vendor.mapID
        if mapID then
            if not self.ByMapID[mapID] then
                self.ByMapID[mapID] = {}
            end
            table.insert(self.ByMapID[mapID], npcID)
        end

        -- Index by expansion
        local exp = vendor.expansion
        if exp then
            if not self.ByExpansion[exp] then
                self.ByExpansion[exp] = {}
            end
            table.insert(self.ByExpansion[exp], npcID)
        end

        -- Index by itemID (reverse lookup for tooltip source)
        -- Handle both simple itemIDs and tables with cost data
        if vendor.items then
            for _, item in ipairs(vendor.items) do
                local itemID = type(item) == "table" and item[1] or item
                if itemID then
                    if not self.ByItemID[itemID] then
                        self.ByItemID[itemID] = {}
                        itemCount = itemCount + 1
                    end
                    table.insert(self.ByItemID[itemID], npcID)
                end
            end
        end
    end
    self.VendorCount = vendorCount

    -- Debug output
    if HA.Addon and HA.Addon.db and HA.Addon.db.profile.debug then
        HA.Addon:Debug("VendorDatabase index built:", itemCount, "items from", vendorCount, "vendors")
    end
end

-------------------------------------------------------------------------------
-- Query Functions
-------------------------------------------------------------------------------

-- Direct lookup by NPC ID: O(1)
function VendorDatabase:GetVendor(npcID)
    return self.Vendors[npcID]
end

-- Check if vendor exists
function VendorDatabase:HasVendor(npcID)
    return self.Vendors[npcID] ~= nil
end

-- Get all vendors (cached — result is rebuilt only when InvalidateVendorCache is called)
local cachedAllVendors = nil

function VendorDatabase:GetAllVendors()
    if cachedAllVendors then return cachedAllVendors end
    local all = {}
    for _, vendor in pairs(self.Vendors) do
        all[#all + 1] = vendor
    end
    cachedAllVendors = all
    return all
end

function VendorDatabase:InvalidateVendorCache()
    cachedAllVendors = nil
end

-- Get vendors by map ID
function VendorDatabase:GetVendorsByMapID(mapID)
    local vendors = {}
    local npcIDs = self.ByMapID and self.ByMapID[mapID]
    if npcIDs then
        for _, npcID in ipairs(npcIDs) do
            local vendor = self.Vendors[npcID]
            if vendor then
                table.insert(vendors, vendor)
            end
        end
    end
    return vendors
end

-- Get vendors by expansion
function VendorDatabase:GetVendorsByExpansion(expansion)
    local vendors = {}
    local npcIDs = self.ByExpansion and self.ByExpansion[expansion]
    if npcIDs then
        for _, npcID in ipairs(npcIDs) do
            local vendor = self.Vendors[npcID]
            if vendor then
                table.insert(vendors, vendor)
            end
        end
    end
    return vendors
end

-- Get total vendor count
function VendorDatabase:GetVendorCount()
    if self.VendorCount then
        return self.VendorCount
    end

    local count = 0
    for _ in pairs(self.Vendors) do
        count = count + 1
    end
    self.VendorCount = count
    return count
end

function VendorDatabase:GetAliasCount()
    local staticCount = 0
    for _ in pairs(self.Aliases) do
        staticCount = staticCount + 1
    end
    local discoveredCount = 0
    if HomesteadDB and HomesteadDB.global and HomesteadDB.global.discoveredAliases then
        for _ in pairs(HomesteadDB.global.discoveredAliases) do
            discoveredCount = discoveredCount + 1
        end
    end
    return staticCount, discoveredCount
end

return VendorDatabase
